#!/usr/bin/env python3
"""
Generate a deterministic battery of edge-case test documents for Posey.

The corpus is a stress test of Posey's normalization, segmentation, and
display pipelines. Each document targets ONE specific class of artifact
or content shape so regressions can be located precisely.

Output layout (default ~/.posey-corpus or --output-dir):
    txt/   plain-text edge cases
    md/    Markdown edge cases
    html/  HTML edge cases

Each file's name encodes what it tests, e.g. `txt/04_nbsp.txt`,
`txt/12_spaced_uppercase.txt`. The verification harness
(verify_synthetic_corpus.py) imports each file via the local API and
checks the post-normalization output for expected properties.

PDF, EPUB, and DOCX edge cases are generated by a sibling Swift script
(`generate_pdf_corpus.swift`) and an EPUB/DOCX zip helper, both of which
this Python script can invoke when --include-rich is passed.

Usage:
    python3 tools/generate_test_docs.py [--output-dir DIR] [--include-rich]
                                        [--formats txt,md,html,pdf,epub,docx]
                                        [--list]

Examples:
    # Generate everything to the default location
    python3 tools/generate_test_docs.py

    # Just TXT and MD
    python3 tools/generate_test_docs.py --formats txt,md

    # List planned generators without writing anything
    python3 tools/generate_test_docs.py --list

The generators are deterministic — running twice produces byte-identical
output. Safe to commit the corpus to a fixture repo or regenerate on CI.
"""
from __future__ import annotations

import argparse
import os
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Callable, Dict, List

DEFAULT_OUTPUT_DIR = Path.home() / ".posey-corpus"

# ----------------------------------------------------------------------
# Sentence stock — short, varied, deterministic. Reused across generators
# so the only variable is the artifact being tested, not the prose.
# ----------------------------------------------------------------------
PROSE_LINES = [
    "The reader settled into the chair and opened the book.",
    "Difficult prose rewards patience above all else.",
    "Notes anchored to the source remember themselves.",
    "Some sentences breathe; others gasp.",
    "The cadence of careful argument is itself an argument.",
    "When read aloud, prose finds its truer shape.",
    "A page of dense text is also a topography.",
    "The eye learns the page; the mind learns the thought.",
    "Reading is a conversation conducted across centuries.",
    "Footnotes are the marginalia of the disciplined.",
]

ARTIFACT_GENERATORS: List["DocSpec"] = []


@dataclass
class DocSpec:
    """One test document to generate."""
    fmt: str              # "txt" | "md" | "html" | "pdf" | "epub" | "docx"
    slug: str             # filename without extension; numeric prefix for ordering
    summary: str          # one-line description of what this document tests
    builder: Callable[[], bytes]   # returns raw bytes for the file

    @property
    def filename(self) -> str:
        return f"{self.slug}.{self.fmt}"


def register(spec: DocSpec) -> None:
    ARTIFACT_GENERATORS.append(spec)


# ======================================================================
# TXT-format generators
# ======================================================================

def _txt(content: str) -> bytes:
    return content.encode("utf-8")


def _txt_bom(content: str) -> bytes:
    return ("﻿" + content).encode("utf-8")


def _build_baseline_clean() -> bytes:
    return _txt("\n\n".join(PROSE_LINES) + "\n")


def _build_soft_hyphens() -> bytes:
    # U+00AD scattered through every word. Should be stripped to invisibility.
    soft = "­"
    text_lines = []
    for line in PROSE_LINES:
        out = []
        for word in line.split(" "):
            if len(word) >= 4:
                mid = len(word) // 2
                out.append(word[:mid] + soft + word[mid:])
            else:
                out.append(word)
        text_lines.append(" ".join(out))
    return _txt("\n\n".join(text_lines) + "\n")


def _build_line_break_hyphens() -> bytes:
    # Simulate PDF wrap: word ends with hyphen, next line starts with lowercase.
    # Should collapse to single word.
    pairs = [
        ("inde- pendent", "independent"),
        ("under- standing", "understanding"),
        ("trans- lation", "translation"),
        ("re- mark", "remark"),
        ("dem- onstration", "demonstration"),
    ]
    lines = []
    for src, _ in pairs:
        lines.append(f"The {src} of careful reading matters.")
    return _txt("\n\n".join(lines) + "\n")


def _build_logical_not_hyphens() -> bytes:
    # Some PDF generators use ¬ (U+00AC) as a line-break marker.
    pairs = [
        "indepen¬ dent",
        "under¬ standing",
        "trans¬ lation",
    ]
    lines = [f"Words like {p} are common in old typesetting." for p in pairs]
    return _txt("\n\n".join(lines) + "\n")


def _build_nbsp() -> bytes:
    # Non-breaking space ( ) between every word.
    nbsp = " "
    lines = [line.replace(" ", nbsp) for line in PROSE_LINES]
    return _txt("\n\n".join(lines) + "\n")


def _build_zwsp() -> bytes:
    # Zero-width space (​) inserted in random spots within words.
    zwsp = "​"
    lines = [line.replace("the", "th" + zwsp + "e") for line in PROSE_LINES]
    return _txt("\n\n".join(lines) + "\n")


def _build_bom() -> bytes:
    # BOM (﻿) at the file start.
    return _txt_bom("\n\n".join(PROSE_LINES) + "\n")


def _build_tabs() -> bytes:
    # Tabs everywhere instead of spaces.
    lines = [line.replace(" ", "\t") for line in PROSE_LINES]
    return _txt("\n\n".join(lines) + "\n")


def _build_mixed_line_endings() -> bytes:
    # Mix CRLF, CR, LF in the same file.
    parts = []
    for i, line in enumerate(PROSE_LINES):
        if i % 3 == 0:
            parts.append(line + "\r\n")
        elif i % 3 == 1:
            parts.append(line + "\r")
        else:
            parts.append(line + "\n")
    return "".join(parts).encode("utf-8")


def _build_trailing_whitespace() -> bytes:
    # Every line ends with trailing spaces and tabs.
    lines = [f"{line}   \t  " for line in PROSE_LINES]
    return _txt("\n\n".join(lines) + "\n")


def _build_excessive_blank_lines() -> bytes:
    # Many blank lines between paragraphs — should collapse to standard separator.
    return _txt(("\n" * 8).join(PROSE_LINES) + "\n")


def _build_spaced_uppercase() -> bytes:
    # PDF artifact: capitals separated by spaces.
    headings = ["C O N T E N T S", "I N T R O D U C T I O N", "C H A P T E R   O N E"]
    body = "\n\n".join(PROSE_LINES[:4])
    return _txt("\n\n".join(headings) + "\n\n" + body + "\n")


def _build_spaced_lowercase() -> bytes:
    headings = ["t a b l e   o f   c o n t e n t s", "p a g e   n u m b e r s"]
    body = "\n\n".join(PROSE_LINES[:4])
    return _txt("\n\n".join(headings) + "\n\n" + body + "\n")


def _build_spaced_accented() -> bytes:
    # Accented capitals — exercises the Unicode property class \p{Lu}.
    return _txt("P A S A R Á N\n\n" + "\n\n".join(PROSE_LINES[:4]) + "\n")


def _build_spaced_digits() -> bytes:
    # Digits separated by spaces (e.g. published years).
    lines = [
        "The book was written in 1 9 4 5 and revised in 1 9 6 7.",
        "Chapter 4 addresses 2 0 0 1 specifically.",
    ]
    return _txt("\n\n".join(lines) + "\n")


def _build_ligatures() -> bytes:
    # Unicode ligatures fi, fl, ffi.
    text = "The oﬃcial figure shows fiﬂtration is eﬃcient.\n\n"
    text += "fi: ﬁ fl: ﬂ ffi: ﬃ ffl: ﬄ\n"
    return _txt(text)


def _build_mixed_scripts() -> bytes:
    # English + Cyrillic + Greek + Arabic + CJK in the same paragraph.
    lines = [
        "The English word for 'book' is book.",
        "The Russian word is книга.",
        "The Greek word is βιβλίο.",
        "The Arabic word is كتاب.",
        "The Chinese word is 书.",
    ]
    return _txt("\n\n".join(lines) + "\n")


def _build_emoji() -> bytes:
    lines = [
        "The reader \U0001f4d6 settled into the chair \U0001fa91.",
        "Some sentences ✨ sparkle; others fade \U0001f4ad.",
    ]
    return _txt("\n\n".join(lines) + "\n")


def _build_combining_diacritics() -> bytes:
    # Same word with combining diacritic vs precomposed form.
    text = (
        "Café (precomposed)\n\n"  # U+00E9
        "Café (e + combining acute)\n\n"  # U+0065 + U+0301
        "These should look identical and read identically.\n"
    )
    return _txt(text)


def _build_rtl_mixed() -> bytes:
    text = (
        "The Arabic phrase مرحبا means hello.\n\n"
        "Hebrew שלום is also peace.\n"
    )
    return _txt(text)


def _build_empty() -> bytes:
    return b""


def _build_only_whitespace() -> bytes:
    return _txt("    \n\n\t\n  \n")


def _build_one_char() -> bytes:
    return _txt("A")


def _build_only_punctuation() -> bytes:
    # Tests segmenter behavior when there are no actual word characters.
    return _txt(". , ; : ! ? — – ... .,!?\n\n.,;:!?\n")


def _build_very_long_no_punct() -> bytes:
    # 2500-char "sentence" with no sentence-ending punctuation.
    word = "indistinguishable"
    text = (word + " ") * 130
    return _txt(text.strip() + "\n")


def _build_dot_leader_toc() -> bytes:
    # TOC with classic dot leaders: "Chapter 1 . . . . . 5"
    lines = [
        "Table of Contents",
        "",
        "Chapter 1 . . . . . . . . . . . . . . . . . 5",
        "Chapter 2 . . . . . . . . . . . . . . . . 23",
        "Chapter 3 . . . . . . . . . . . . . . . . 47",
        "Chapter 4 . . . . . . . . . . . . . . . . 71",
        "Index . . . . . . . . . . . . . . . . . . 95",
    ]
    return _txt("\n".join(lines) + "\n")


def _build_only_page_numbers() -> bytes:
    return _txt("1\n\n2\n\n3\n\n4\n\n5\n")


def _build_repeated_boilerplate() -> bytes:
    # Same watermark line repeated as if extracted from each PDF page.
    line = "(c) 2024 Example Press. All rights reserved."
    return _txt(("\n\n".join([line] * 20)) + "\n")


def _build_very_long_document() -> bytes:
    # ~100 KB of varied prose, paragraph-separated.
    body = "\n\n".join(PROSE_LINES * 200)
    return _txt(body + "\n")


def _build_unbalanced_quotes() -> bytes:
    # Smart quotes opened but not closed; mixed straight + curly.
    text = (
        "She said, “The text is broken,” and meant it.\n\n"
        "Then he replied: “Is it really? without closing the quote\n\n"
        "And finally a 'straight' “curly” mix.\n"
    )
    return _txt(text)


def _build_long_url() -> bytes:
    # Wayback-machine-style URL block — known pattern from the audit.
    url = "https://web.archive.org/web/20240101000000/https://example.com/very/long/path/to/an/article/that/has/many/segments?and=query&parameters=here&moreThings=true"
    text = f"Reference: {url}\n\nSee also: {url}\n"
    return _txt(text)


# Register TXT generators in deterministic order.
