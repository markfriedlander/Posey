# ======================================================================
# CLI
# ======================================================================

def _selected_specs(formats: List[str]) -> List[DocSpec]:
    if not formats:
        return ARTIFACT_GENERATORS
    return [s for s in ARTIFACT_GENERATORS if s.fmt in formats]


def main(argv: List[str]) -> int:
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--output-dir", type=Path, default=DEFAULT_OUTPUT_DIR,
                        help=f"where to write the corpus (default: {DEFAULT_OUTPUT_DIR})")
    parser.add_argument("--formats", default="",
                        help="comma-separated subset of formats to generate (txt,md,html,rtf,...). Empty = all.")
    parser.add_argument("--list", action="store_true",
                        help="list every spec without writing files")
    args = parser.parse_args(argv)

    formats = [f.strip().lower() for f in args.formats.split(",") if f.strip()]
    specs = _selected_specs(formats)

    if args.list:
        by_fmt: Dict[str, List[DocSpec]] = {}
        for s in specs:
            by_fmt.setdefault(s.fmt, []).append(s)
        for fmt in sorted(by_fmt):
            print(f"\n[{fmt}] ({len(by_fmt[fmt])} docs)")
            for s in by_fmt[fmt]:
                print(f"  {s.filename:40} {s.summary}")
        return 0

    if not specs:
        print(f"No generators match formats={formats!r}", file=sys.stderr)
        return 2

    out = args.output_dir
    out.mkdir(parents=True, exist_ok=True)
    written = 0
    for spec in specs:
        sub = out / spec.fmt
        sub.mkdir(exist_ok=True)
        target = sub / spec.filename
        target.write_bytes(spec.builder())
        written += 1
        print(f"  wrote {target.relative_to(out)}  ({spec.summary})")

    print(f"\nDone — {written} documents in {out}")
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
